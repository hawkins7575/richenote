SELECT 
  id,
  title,
  property_type,
  transaction_type,
  CASE 
    WHEN price IS NOT NULL THEN price || '만원'
    WHEN monthly_rent IS NOT NULL THEN deposit || '/' || monthly_rent || '만원'
    WHEN deposit IS NOT NULL THEN deposit || '만원 전세'
    ELSE '가격정보없음'
  END as price_info,
  status,
  created_at
FROM properties 
ORDER BY created_at DESC;

